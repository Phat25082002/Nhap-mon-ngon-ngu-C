#include <stdio.h>

int main(void) {
  // khai bao bien V va T;
  int V, T;
  // hien thi nhap V =
  printf (" nhap Van = ");
  scanf (" %d", &V);
  // hien thi nhap T =
  printf (" nhap Toan = ");
  scanf ("%d", &T);
  // Tinh diem trung binh Van, Toan va hien ra man hinh V + T/2 = DTB
  printf ("Van + Toan/2 = DBT");
  printf ("\n");
  printf ("Van \t Toan \t DTB");
  return 0;
}
